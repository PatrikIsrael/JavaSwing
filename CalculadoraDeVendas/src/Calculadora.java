    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            double vendaValor = Double.parseDouble(jTextField1.getText());

            if (vendaValor > 500) {
                String descontoStr = JOptionPane.showInputDialog(this, "Informe o percentual de desconto:", "Desconto", JOptionPane.PLAIN_MESSAGE);

                if (descontoStr != null && !descontoStr.isEmpty()) {
                    double desconto = Double.parseDouble(descontoStr);
                    double valorComDesconto = vendaValor * (1 - desconto / 100);

                    JOptionPane.showMessageDialog(this, "Valor Total com Desconto: " + valorComDesconto, "Total", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Percentual de desconto inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Valor Total: " + vendaValor, "Total", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um valor numérico válido.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

